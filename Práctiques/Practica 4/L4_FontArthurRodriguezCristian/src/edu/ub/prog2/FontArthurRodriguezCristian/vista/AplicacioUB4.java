    private void btnEliminarImatgeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarImatgeActionPerformed
        // TODO add your handling code here:
        int[] arrayIndex;
        arrayIndex = jListBiblioteca.getSelectedIndices();
        for (int i = arrayIndex.length-1; i >= 0; i--) {
            try {
                control.esborrarFitxer(arrayIndex[i]);
            } catch (AplicacioException except) {
                except.getMessage();
            }
            finally {
                if (model.getSize()-1 == control.getSize()){

                    while (modelAlbum.contains(model.getElementAt(arrayIndex[i]))) {
                        modelAlbum.removeElement(model.getElementAt(arrayIndex[i]));
                    }
                    model.remove(arrayIndex[i]);
                }
            }
        }
    }//GEN-LAST:event_btnEliminarImatgeActionPerformed

