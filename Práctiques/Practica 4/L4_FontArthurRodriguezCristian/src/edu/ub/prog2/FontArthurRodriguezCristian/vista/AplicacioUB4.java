    private void jComboBoxAlbumsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxAlbumsActionPerformed
        // TODO add your handling code here:
        int i = this.jComboBoxAlbums.getSelectedIndex();
        if (i != -1){
            jListAlbum.setBorder(javax.swing.BorderFactory.createTitledBorder(jComboBoxAlbums.getItemAt(i)));
            try {
                modelAlbum.removeAllElements();
                for (int j = 0; j < control.getSizeAlbum(this.jComboBoxAlbums.getSelectedItem().toString()); j++) {
                    try {
                        modelAlbum.addElement(control.mostrarAlbum(this.jComboBoxAlbums.getSelectedItem().toString()).get(j));
                    } catch (AplicacioException ex) {}
                }
            }catch (AplicacioException ex) {}
        }else {
            jListAlbum.setBorder(javax.swing.BorderFactory.createTitledBorder(""));
            modelAlbum.removeAllElements();
        }
    }//GEN-LAST:event_jComboBoxAlbumsActionPerformed

