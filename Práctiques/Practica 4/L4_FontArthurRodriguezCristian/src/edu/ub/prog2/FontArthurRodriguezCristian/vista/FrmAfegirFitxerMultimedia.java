    private void jButtonChooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChooseFileActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filtermp3mp4 = new FileNameExtensionFilter("MP3/MP4","mp3", "mp4");
        FileNameExtensionFilter filtermp3 = new FileNameExtensionFilter("MP3","mp3");
        FileNameExtensionFilter filtermp4 = new FileNameExtensionFilter("MP4","mp4");
        chooser.setAcceptAllFileFilterUsed(false);
        chooser.addChoosableFileFilter(filtermp3mp4);
        chooser.addChoosableFileFilter(filtermp3);
        chooser.addChoosableFileFilter(filtermp4);

        int returnVal = chooser.showOpenDialog(null);
        if (chooser.getSelectedFile() != null) {
            textCami.setText(chooser.getSelectedFile().getAbsolutePath().split("/" + chooser.getSelectedFile().getName())[0]);
            if (chooser.getSelectedFile().getName().substring(chooser.getSelectedFile().getName().length()-3).equals("mp3")) {
                comboOptionTipus.setSelectedIndex(0);
                textNomAudio.setText(chooser.getSelectedFile().getName());
                textCodecAudio.setText("codec");
                textDuradaAudio.setText("10");
                textImatge.setText("img");
                textKbps.setText("10");
            }else {
                comboOptionTipus.setSelectedIndex(1);
                textNomVideo.setText(chooser.getSelectedFile().getName());
                textCodecVideo.setText("codec");
                textDuradaVideo.setText("10");
                textAlcada.setText("10");
                textAmplada.setText("10");
                textFps.setText("10");
            }
        }
    }//GEN-LAST:event_jButtonChooseFileActionPerformed
    
