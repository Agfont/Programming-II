    private void btnAcceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptarActionPerformed
        // TODO add your handling code here
        if (this.comboOptionTipus.getSelectedItem().toString().equals("Video")) {
            if (this.textCami.getText().length() == 0 || this.textNomVideo.getText().length() == 0  || this.textCodecVideo.getText().length() == 0
                || this.textDuradaVideo.getText().length() == 0  ||  this.textAlcada.getText().length() == 0  || this.textAmplada.getText().length() == 0
                || this.textFps.getText().length() == 0 ) {
                etCampsBuits.setText("Omplir els camps buits");
            }
            else {
                try {
                    control.afegirVideo(this.textCami.getText() + "/" + this.textNomVideo.getText(),
                        this.textNomVideo.getText(), this.textCodecVideo.getText(),
                        Float.parseFloat(this.textDuradaVideo.getText()),
                        Integer.parseInt(this.textAlcada.getText()),
                        Integer.parseInt(this.textAmplada.getText()),
                        Float.parseFloat(this.textFps.getText()) );
                }
                catch (AplicacioException except) {
                    except.getMessage();
                }
                this.setVisible(false);
            }
        }
        else {
            if (this.textCami.getText().length() == 0 || this.textNomAudio.getText().length() == 0  || this.textCodecAudio.getText().length() == 0
                || this.textDuradaAudio.getText().length() == 0  ||  this.textImatge.getText().length() == 0  || this.textKbps.getText().length() == 0) {
                etCampsBuits.setText("*Omplir els camps buits");
            }
            else {
                try {
                    control.afegirAudio(this.textCami.getText() + "/" + this.textNomAudio.getText(),
                        this.textImatge.getText(),
                        this.textNomAudio.getText(),
                        this.textCodecAudio.getText(),
                        Float.parseFloat(this.textDuradaAudio.getText()),
                        Integer.parseInt(this.textKbps.getText()));
                }
                catch (AplicacioException except) {
                    except.getMessage();
                }
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_btnAcceptarActionPerformed

